public class Switchtest {
    public static void main(String args[]){
        int num =1;
        switch(num){
            case 0:
                System.out.println("Number is ZERO");
                break;
            case 1:
                System.out.println(("Number is ONE"));
                break;
            case 2:
                System.out.println("Number is 3");
                break;
            default:
                System.out.println("This is default value");
        }
    }
}
